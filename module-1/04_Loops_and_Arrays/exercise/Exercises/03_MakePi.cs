    public partial class Exercises
    {
        /*
         Return an int array length 3 containing the first 3 digits of pi, {3, 1, 4}.
         MakePi() → [3, 1, 4]
         */
        public int[] MakePi()
        {
            int[] makePi = new int[3];
            makePi[0] = 3;
            makePi[1] = 1;
            makePi[2] = 4;

            return makePi;
        }
    }
